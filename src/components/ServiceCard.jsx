// ServiceCard.jsx
// ПОЧЕМУ? Функциональный компонент — это чистая функция, принимающая props.
// Имя с заглавной буквы — обязательное правило React для распознавания компонентов.
// ПОЧЕМУ деструктуризация? Мы сразу «распаковываем» нужные поля из props,
// чтобы код был короче и сразу было видно, какие данные ожидает компонент.

// ПОЧЕМУ? Импортируем TimeSlot и BookingForm — они будут использоваться
// для расширения функционала карточки при клике (принцип декомпозиции:
// карточка отвечает за отображение, дочерние компоненты — за интерактив).
import React, { useState } from "react";
import TimeSlot from "./TimeSlot";
import BookingForm from "./BookingForm";

// ПОЧЕМУ? Данные вынесены в константу внутри файла для учебного примера.
// В реальном проекте они будут приходить из API или localStorage через props.
const flatList = [
  { number: 1, price: 100, address: 'Минск, ул. Притыцкого, 60' },
  { number: 2, price: 90, address: 'Минск, ул. Кульман, 5' },
  { number: 3, price: 120, address: 'Минск, пр. Независимости, 95' },
  { number: 4, price: 170, address: 'Минск, ул. Мельникайте, 2' },
  { number: 5, price: 110, address: 'Минск, ул. Сурганова, 43' },
];

// ПОЧЕМУ? Компонент назван ServiceCard, чтобы отражать тему курсового проекта
// («Система онлайн-бронирования помещений»), а не учебный пример с курсами.
const ServiceCard = (props) => {
  // ПОЧЕМУ? useState используется для локального состояния «открытая карточка».
  // Это позволяет показывать форму бронирования только для выбранного помещения,
  // не затрагивая другие карточки (принцип инкапсуляции состояния).
  const [state, setState] = useState({ openedFlat: null });

  return (
    // ПОЧЕМУ? Фрагмент <>...</> используется для группировки элементов без
    // создания лишнего div в DOM — это улучшает производительность и валидность вёрстки.
    <>
      <TimeSlot />
      <div className={'flat_card_wrapper'}>
        {flatList.map((flat) => (
          // ПОЧЕМУ? key={flat.number} — уникальный идентификатор для React,
          // чтобы при обновлении списка перерисовывались только изменившиеся элементы.
          <div
            className={'flat_card'}
            key={flat.number}
            onClick={() => setState({ ...state, openedFlat: flat })}
          >
            {/* ПОЧЕМУ? Пустой src — заглушка для изображения. В реальном проекте
                здесь будет URL фотографии помещения из данных или API. */}
            <img src={''} alt={`Фото помещения №${flat.number}`} width={'250px'} />
            <p>Помещение №{flat.number}</p>
            <p>{flat.price} BYN / час</p>
          </div>
        ))}
      </div>
      {/* ПОЧЕМУ? Условный рендеринг &&: форма показывается только если
          в state есть выбранное помещение. Это экономит ресурсы и упрощает UX. */}
      {state.openedFlat && <BookingForm roomData={state.openedFlat} />}
    </>
  );
};

export default ServiceCard;